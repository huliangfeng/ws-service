package com.nw.service.entity;

import org.springframework.util.StringUtils;

public class PetImmune {

	/**
	 * 疫苗种类
	 */
	private String vaccineType;
	
	/**
	 * 疫苗名称
	 */
	private String vaccineName;
	
	/**
	 * 注射点
	 */
	private String injectOrg;
	
	/**
	 * 负责人(录入免疫信息的人)
	 */
	private String injectUser;
	
	/**
	 * 注射时间
	 */
	private String injectTime;
	
	/**
	 * 下次疫苗免疫时间
	 */
	private String nextImmuneTime;
	
	public void initDefaultObject() {
		if (StringUtils.isEmpty(this.vaccineType)) {
			this.vaccineType = "";
		}
		if (StringUtils.isEmpty(this.vaccineName)) {
			this.vaccineName = "";
		}
		if (StringUtils.isEmpty(this.injectOrg)) {
			this.injectOrg = "";
		}
		if (StringUtils.isEmpty(this.injectUser)) {
			this.injectUser = "";
		}
		if (StringUtils.isEmpty(this.injectTime)) {
			this.injectTime = "";
		}
		if (StringUtils.isEmpty(this.nextImmuneTime)) {
			this.nextImmuneTime = "";
		}
	}

	public String getVaccineType() {
		return vaccineType;
	}

	public void setVaccineType(String vaccineType) {
		this.vaccineType = vaccineType;
	}

	public String getVaccineName() {
		return vaccineName;
	}

	public void setVaccineName(String vaccineName) {
		this.vaccineName = vaccineName;
	}

	public String getInjectOrg() {
		return injectOrg;
	}

	public void setInjectOrg(String injectOrg) {
		this.injectOrg = injectOrg;
	}

	public String getInjectUser() {
		return injectUser;
	}

	public void setInjectUser(String injectUser) {
		this.injectUser = injectUser;
	}

	public String getInjectTime() {
		return injectTime;
	}

	public void setInjectTime(String injectTime) {
		this.injectTime = injectTime;
	}

	public String getNextImmuneTime() {
		return nextImmuneTime;
	}

	public void setNextImmuneTime(String nextImmuneTime) {
		this.nextImmuneTime = nextImmuneTime;
	}
	
	
}
